    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        UsersModel model = new UsersModel();
        model.nik = Integer.parseInt(this.jLabel1.getText());
        model.name= this.jTextField1.getText();
        model.password =  utilLib.hash(this.jPasswordField1.getText());
        try
        {
            model.id_role = Integer.parseInt(sqlLib.searchData4("roles", new String[] {"id"},String.format("where role='%s';",this.jComboBox1.getSelectedItem().toString() ), RolesModel.class).get(0).loadValues("id").toString());
        }
        catch (Exception ex)
        {
            model.id_role = usr.id_role;
        }                        
        System.out.println("HASIL:" + String.join(",", model.loadAllValues()));
        switch (action)
        {
            case CREATE:
                sqlLib.InsertData("users", model);
                break;
            case UPDATE:
                sqlLib.UpdateData("users", model,"nik");
                break;
            case DELETE:
                sqlLib.DeleteData("users", model, "nik");
                break;
        }
        mng.loadTABLE();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

