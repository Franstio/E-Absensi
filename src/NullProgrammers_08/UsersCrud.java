    private void populateComboBox()
    {
        this.jComboBox1.removeAllItems();
        this.jComboBox1.addItem("--Role--");
        List<Imodels> model = sqlLib.searchData4("roles", new String[]{"*"}, "", RolesModel.class);
        for (Imodels mod : model)
        {
            this.jComboBox1.addItem(mod.loadValues("role").toString());
        }
    }
    private void initialize()
    {
        populateComboBox();
        this.jLabel3.setText(action.name() + " Users");
        switch (action)
        {
            case CREATE:
                this.jLabel1.setText(utilLib.generateCode("", "users", "nik", UsersModel.class));
                break;
            case DELETE:
                this.jTextField1.setEditable(false);
                this.jPasswordField1.setEditable(false);
                this.jComboBox1.setEnabled(false);
            case UPDATE:
                this.jLabel1.setText(usr.nik.toString());
                this.jTextField1.setText(usr.name);
                break;
        }
    }
