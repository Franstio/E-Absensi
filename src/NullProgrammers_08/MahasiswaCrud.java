    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        MahasiswaModel mod = new MahasiswaModel();        
        mod.nim = this.jLabel1.getText();
        mod.name = this.jTextField1.getText();
        mod.age = Integer.parseInt(this.jTextField2.getText());
        mod.semester = Integer.parseInt(this.jSpinner1.getValue().toString());
        try
        {
            mod.id_kelas = sqlLib.searchData4("kelas", new String[]{"kelas.id"}, String.format("inner join prodi p on p.id=kelas.id_prodi inner join jurusan j on p.id_jurusan=j.id where kelas.name='%s' and p.name='%s' and j.name='%s';",this.jComboBox3.getSelectedItem().toString(),this.jComboBox2.getSelectedItem().toString(),this.jComboBox1.getSelectedItem().toString()), KelasModel.class).get(0).loadValues("id").toString();
        }
        catch(Exception ex)
        {
            mod.id_kelas = model.id_kelas;
        }
        switch (action)
        {
            case CREATE:
                sqlLib.InsertData("mahasiswa",mod);
                break;
            case DELETE:
                sqlLib.DeleteData("mahasiswa", mod, "nim");
                break;
            case UPDATE:
                sqlLib.UpdateData("mahasiswa", mod, "nim");
                break;
        }
        mng.loadTable(4, "mahasiswaView", MahasiswaView.class);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

