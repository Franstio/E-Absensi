    private void populateComboBox()
    {
        this.jComboBox1.removeAllItems();
        this.jComboBox2.removeAllItems();
        this.jComboBox3.removeAllItems();
        this.jComboBox2.addItem("Prodi");
        this.jComboBox3.addItem("Kelas");
        List<Imodels> mods = sqlLib.searchData4("Jurusan", new String[]{"*"}, "", JurusanModel.class);
        for (Imodels mod : mods)
        {
            this.jComboBox1.addItem(mod.loadValues("name").toString());
        }
        this.jComboBox1.addItemListener(new ItemListener(){
            @Override
            public void itemStateChanged(ItemEvent e) {
                //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
                List<Imodels> mods2 = sqlLib.searchData4("Prodi", new String[]{"*"}, String.format(" inner join jurusan on prodi.id_jurusan=jurusan.id where jurusan.name='%s';",jComboBox1.getSelectedItem().toString()), ProdiModel.class);
                if (mods2 != null&& mods2.size()>0)
                {
                    jComboBox2.removeAllItems();
                    for (Imodels mod : mods2)
                    {
                        jComboBox2.addItem(mod.loadValues("name").toString());
                    }
                    jComboBox2.setSelectedIndex(0);
                }             
            }
                        
        });
        jComboBox2.addItemListener(new ItemListener(){
                @Override
                public void itemStateChanged(ItemEvent e) {
                //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
                System.out.println(e.getItem().toString());
                List<Imodels> mods3 = sqlLib.searchData4("kelas", new String[]{"kelas.*"}, String.format("inner join prodi on prodi.id=kelas.id_prodi where prodi.name='%s';",e.getItem().toString()), KelasModel.class);
                if (mods3 != null && mods3.size() > 0)
                {
                    for (Imodels mod : mods3)
                    {                
                        jComboBox3.removeAllItems();
                        jComboBox3.addItem(mod.loadValues("name").toString());
                    }
                }
            }
        });
    }
