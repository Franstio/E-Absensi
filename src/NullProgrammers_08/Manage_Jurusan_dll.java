    public <T extends ModelsBase >void loadTable(int tableIndex,String name,Class<T> ct)
    {
        List<Imodels> model = sqlLib.searchData4(name, new String[]{"*"}, "", ct);
        if (model.size() == 0 || model == null)
        {
            try
            {
                model.add(ct.newInstance());
            }
            catch(Exception ex)
            {
                System.err.println(ex);
            }
        }
        JTable jtable = null;
        switch (tableIndex)
        {
            case 1:
                jtable = this.jTable1;
                break;
            case 2:
                jtable = this.jTable2;
                break;
            case 3:
                jtable = this.jTable3;
                break;
            case 4:
                jtable = this.jTable4;
                break;
        }
        DefaultTableModel table = utilLib.loadTable(model);
        jtable.setModel(table);
    }
