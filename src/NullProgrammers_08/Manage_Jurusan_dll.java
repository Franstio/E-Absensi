    {
        DefaultTableModel table = (DefaultTableModel)jt.getModel();
        Dictionary dict = new Hashtable();
        for (int i=0;i<table.getColumnCount();i++)
        {
            System.out.println("jt: " + table.getColumnName(i) + " " + table.getValueAt(jt.getSelectedRow(), i));
            dict.put(table.getColumnName(i), table.getValueAt(jt.getSelectedRow(), i));
            System.out.println("END" + i);
        }
        return dict;
    }
