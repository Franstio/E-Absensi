    private void populateComboBox()
    {
        this.jComboBox1.removeAllItems();
        this.jComboBox2.removeAllItems();
        this.jComboBox2.addItem("Prodi");
        List<Imodels> model1=null;
        model1 = sqlLib.searchData4("jurusan", new String[]{"*"}, "", JurusanModel.class);
        for (Imodels model : model1)
        {
            this.jComboBox1.addItem(model.loadValues("name").toString());
        }
        this.jComboBox1.setSelectedIndex(0);
        this.jComboBox1.addItemListener(new ItemListener(){
            @Override
            public void itemStateChanged(ItemEvent e) {
//                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.            
                jComboBox2.removeAllItems();
                System.out.println(jComboBox1.getSelectedItem().toString());
                List<Imodels> model2 = sqlLib.searchData4("prodi", new String[]{"prodi.*"}, String.format(" inner join jurusan on jurusan.id=prodi.id_jurusan where jurusan.name='%s';",jComboBox1.getItemAt(jComboBox1.getSelectedIndex())).toString(), ProdiModel.class);
                if (model2 != null)
                {
                    for (Imodels model : model2)
                    {
                        jComboBox2.addItem(model.loadValues("name").toString());
                    }
                }
            }
            
        });
    }
    private void initialize()
    {
        this.populateComboBox();
        switch(action)
        {
            case CREATE:
                this.jLabel1.setText(utilLib.generateCode("KLS", "kelas", "id", KelasModel.class));
                break;
            case DELETE:
                this.jTextField1.setEditable(false);
                this.jTextField1.setEditable(false);
                this.jComboBox1.setEnabled(false);
                this.jComboBox2.setEnabled(false);
                this.jSpinner1.setEnabled(false);
                
            case UPDATE:
                this.jLabel1.setText(kls.id);
                this.jTextField1.setText(kls.name);
                this.jSpinner1.setValue(kls.semester);
                break;
        }
    }
