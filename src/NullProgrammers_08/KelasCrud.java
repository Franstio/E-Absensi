    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        KelasModel km = new KelasModel();
        km.id = this.jLabel1.getText();
        km.name = this.jTextField1.getText();
        km.semester = Integer.parseInt(this.jSpinner1.getValue().toString());
        try
        {
            km.id_prodi = sqlLib.searchData4("prodi", new String[]{"id"}, String.format(" where name='%s';",this.jComboBox2.getSelectedItem().toString()), KelasModel.class).get(0).loadValues("id").toString();
        }
        catch(Exception ex)
        {
            km.id_prodi = kls.id_prodi;
        }
        System.out.println(String.join(",", km.loadAllValues()));
        switch (action)
        {
            case CREATE:
                sqlLib.InsertData("kelas",km);
                break;
            case DELETE:
                sqlLib.DeleteData("kelas", km, "id");
                break;
            case UPDATE:
                sqlLib.UpdateData("kelas", km, "id");
                break;
        }
        mng.loadTable(3, "kelasView", KelasView.class);
        JOptionPane.showMessageDialog(this,action.toString() + " sucessfully");
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

